using System;
class Program
{
    static void Main()
    {
        // Информация о лабораторной работе
        Console.WriteLine("Лабораторная работа 2");
        Console.WriteLine("Задание 1, Вариант 7: Найти последнюю цифру числа n");
        Console.WriteLine("Дано натуральное число n (n <= 100). Найти последнюю цифру числа n.");
        Console.WriteLine("Используются ТОЛЬКО операции целочисленной арифметики.");
        Console.WriteLine("Пояснение: последняя цифра находится через операцию взятия остатка от деления на 10 (n % 10)");
        Console.WriteLine("Например: 47 % 10 = 7, 100 % 10 = 0, 5 % 10 = 5");
        Console.WriteLine("===========================================");

        // Цикл для повторного ввода
        while (true)
        {
            Console.WriteLine();
            Console.Write("Введите натуральное число n (или 'exit' для выхода): ");

            // Считываем введенное значение
            string input = Console.ReadLine();

            // Проверяем, хочет ли пользователь выйти
            if (input.ToLower() == "exit" || input.ToLower() == "выход")
            {
                Console.WriteLine("Выход из программы.");
                break;
            }

            // Преобразуем в целое число
            if (int.TryParse(input, out int n))
            {
                // Проверяем, что число натуральное (положительное)
                if (n <= 0)
                {
                    Console.WriteLine("Ошибка: введите натуральное число (больше 0).");
                    continue;
                }

                // Последняя цифра числа n находится с помощью операции деления по модулю на 10
                int lastDigit = n % 10;

                // Выводим результат на экран
                Console.WriteLine("Последняя цифра числа " + n + ": " + lastDigit);
            }
            else
            {
                Console.WriteLine("Ошибка: введено некорректное значение.");
            }
        }

        // Ждём нажатия клавиши перед закрытием
        Console.WriteLine("\nНажмите любую клавишу для завершения...");
        Console.ReadKey();
    }
}